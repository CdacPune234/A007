
public class operation {


	public static int addInt(int num1,int num2)
	{
		return  num1 + num2;

	}
	public static float addFloat(float num1,float num2)
	{
		return  num1 + num2;

	}
	public static int subInt(int num1,int num2)
	{
		return  num1 - num2;

	}
	public static float subFloat(float num1,float num2)
	{
		return  num1 - num2;

	}
	public static int mulInt(int num1,int num2)
	{
		return  num1 * num2;

	}
	public static float mulFloat(float num1,float num2)
	{
		return  num1 * num2;

	}
	
	public static int divInt(int num1,int num2)
	{
		return  num1 / num2;

	}
	public static float divFloat(float num1,float num2)
	{
		return  num1 / num2;

	}


}
